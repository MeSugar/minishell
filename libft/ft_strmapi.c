#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	size;
	size_t	i;
	char	*result;

	if (s == 0)
		return (0);
	size = ft_strlen(s) + 1;
	i = 0;
	result = (char *)malloc(sizeof(char) * size);
	if (!result)
		return (0);
	while (s[i])
	{
		result[i] = f(i, s[i]);
		i++;
	}
	result[i] = '\0';
	return (result);
}
